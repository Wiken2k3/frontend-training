/**Tips chuyên nghiệp (Day 1)

🟡 Font xu hướng 2025: Sans-serif gọn gàng như Inter, Plus Jakarta Sans, Manrope.

🟡 Màu xu hướng: nền trắng/đen đơn giản, điểm nhấn bằng màu vàng, xanh lá ngọc, hoặc xanh neon.

🟡 Border radius lớn (12px – 24px) → UI hiện đại, mềm mại.

🟡 Shadow nhẹ (0 4px 12px rgba(0,0,0,0.1)) → tạo chiều sâu. */