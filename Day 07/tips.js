/**Tips nhớ lâu (kỹ mà đơn giản)

Flexbox cho hàng/cột đơn; Grid cho lưới nhiều cột.

Dùng gap thay vì thêm margin vào item → khoảng cách ổn định.

Dùng aspect-ratio để ảnh không méo, layout không nhảy.

Tạo thiết kế nhất quán bằng biến CSS (:root) cho màu, radius, shadow.

Kiểm tra trên màn nhỏ: thu hẹp trình duyệt và nhìn card rơi cột mượt chưa. */